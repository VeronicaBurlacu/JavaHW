package review4;

public class ContinueLoop {
    public static void main(String[] args) {
        //CONTINUE=> skip(we use it inseide the( IF STATEMENT)
        for (int i = 1; i <  5; i++) {
            if(i==3){continue;}//gonna print 1,2,4 basically skipped 3
            System.out.println(i);


        }
    }
}
